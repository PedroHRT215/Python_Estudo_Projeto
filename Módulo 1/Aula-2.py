#Como funciona a função de print?

#Para poder exibir os dados no terminal em Python se utiliza 'print'.
print ('Hello Word!')

#Também é possível utilizar a função print para exibir diversos valores ou argumentos:
print ('My favorite color are', 'blue', 'green', 'red')
#O Python adiciona automaticamente um espaço entre cada item quando você os separa com vírgulas

print ('Hello', 'Word!')
print ('Hello  word!')
